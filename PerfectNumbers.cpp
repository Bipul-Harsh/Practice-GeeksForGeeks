class Solution {
  public:
    int isPerfectNumber(long long N) {
        // code here
        if(N==1)
            return 0;
        int sum = 1;
        for(int val=2; val<=sqrt(N); val++)
            if(N%val == 0){
                sum += val;
                sum += N/val;
            }
        return sum == N;
    }
};

