class Solution 
{
    public:
    //Function to find if there is a celebrity in the party or not.
    int celebrity(vector<vector<int> >& M, int n) 
    {
        // code here 
        for(int row=0; row<n; row++){
            for(int col=0; col<n; col++){
                if(M[row][col]){
                    M[col][col]++;
                    M[row][row]--;
                }
            }
        }
        
        for(int ind=0; ind<n; ind++){
            if(M[ind][ind] == n-1)
                return ind;
        }
        return -1;
    }
};

