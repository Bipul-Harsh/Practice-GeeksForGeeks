class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        // code here
        long long unsigned s1 = 0;
        long long unsigned s2 = 0;
        for(int val : A){
            s1 += val;
        }
        
        for(int ind=0; ind<N && s1 >= s2; ind++){
            s1 -= A[ind];
            if(s1 == s2)
                return ind+1;
            s2 += A[ind];
        }
        
        return -1;
    }
};


